package com.example.test;

public class UserData {
    public String name;
    public String email;
    public String phone;
    public String address;
    public String role;

    public UserData() {}

    public UserData(String name, String email, String phone, String address, String role) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
        this.role = role;
    }
}
